    public void TampilBarang(){
        DefaultTableModel isi=new DefaultTableModel();
        isi.addColumn("ID BARANG");
        isi.addColumn("NAMA BARANG");
        isi.addColumn("MERK BARANG");
        try {
            Statement stat = (Statement)EntityDatabase.GetConnection().createStatement();
            res=stat.executeQuery("select * from t_barang");
            while (res.next()) {
                isi.addRow(new Object[]{res.getString("id_barang"),res.getString("nm_barang"),res.getString("merk_barang")});
            }
            jTable1.setModel(isi);
        } catch (Exception e) {
        }
    }

